a = input('Informações sobre o valor: ')
print('O tipo primitivo desse valor é ?', type(a))
print('Ele é um número ?', a.isnumeric())
print('Só tem espaços ? ', a.isspace())
print('É alfabético ? ', a.isalpha())
print('E alfanúmerico ? ', a.isalnum())
print('Está maiscúla ? ', a.isupper())
print('Está Minuscula ? ', a.islower())
print('Está captalizada?', a.istitle())







